

'use client';

import { useState, useEffect, useRef } from 'react';
import { useParams } from 'next/navigation';
import Link from 'next/link';
import { ArrowLeft, Plus, View, ArrowUp, ArrowDown, Shuffle } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Skeleton } from '@/components/ui/skeleton';
import { db } from '@/lib/firebase';
import { doc, getDoc, collection, query, where, getDocs, orderBy, setDoc } from 'firebase/firestore';
import type { Department, ExpenseRequest, Project, UserSettings } from '@/lib/types';
import { useToast } from '@/hooks/use-toast';
import { Card, CardContent } from '@/components/ui/card';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuCheckboxItem,
  DropdownMenuLabel,
  DropdownMenuSeparator,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import {
  Dialog,
  DialogContent,
  DialogHeader,
  DialogTitle,
  DialogTrigger,
  DialogFooter,
  DialogClose,
  DialogDescription,
} from '@/components/ui/dialog';
import { format } from 'date-fns';
import { useAuth } from '@/components/auth/AuthProvider';


const baseTableHeaders = [
    'Request No',
    'Timestamp',
    'Generated by Department',
    'Project Name',
    'Amount',
    'Head of A/c',
    'Sub-Head of A/c',
    'Remarks',
    'Description',
    'Name of the party'
];

export default function DepartmentExpensesPage() {
  const params = useParams();
  const { toast } = useToast();
  const { user } = useAuth();
  const departmentId = params.departmentId as string;
  const settingsKey = `expenses_${departmentId}`;
  
  const isInitialMount = useRef(true);

  const [department, setDepartment] = useState<Department | null>(null);
  const [projects, setProjects] = useState<Project[]>([]);
  const [expenses, setExpenses] = useState<ExpenseRequest[]>([]);
  const [isLoading, setIsLoading] = useState(true);
  const [isSequenceDialogOpen, setIsSequenceDialogOpen] = useState(false);

  // State for column order
  const [columnOrder, setColumnOrder] = useState<string[]>(baseTableHeaders);
  const [columnVisibility, setColumnVisibility] = useState<Record<string, boolean>>(
    baseTableHeaders.reduce((acc, header) => ({ ...acc, [header]: true }), {})
  );

  useEffect(() => {
    if (!user) return;
    const fetchSettings = async () => {
      const settingsRef = doc(db, 'userSettings', user.id);
      const settingsSnap = await getDoc(settingsRef);
      if (settingsSnap.exists()) {
        const settings = settingsSnap.data() as UserSettings;
        const pageSettings = settings.columnPreferences?.[settingsKey];
        if (pageSettings) {
          // Ensure all base headers are present in the loaded settings
          const mergedVisibility = {
            ...baseTableHeaders.reduce((acc, h) => ({ ...acc, [h]: true }), {}),
            ...pageSettings.visibility
          };
          const mergedOrder = [
            ...pageSettings.order,
            ...baseTableHeaders.filter(h => !pageSettings.order.includes(h))
          ];
          setColumnVisibility(mergedVisibility);
          setColumnOrder(mergedOrder);
        }
      }
    };
    fetchSettings();
  }, [user, settingsKey]);

  const saveColumnSettings = async (order: string[], visibility: Record<string, boolean>) => {
    if (!user) return;
    try {
        const settingsRef = doc(db, 'userSettings', user.id);
        const settingsSnap = await getDoc(settingsRef);
        const currentSettings = settingsSnap.exists() ? settingsSnap.data() : { columnPreferences: {} };

        const newPreferences = {
            ...currentSettings.columnPreferences,
            [settingsKey]: {
                order: order,
                visibility: visibility,
            }
        };
        await setDoc(settingsRef, { ...currentSettings, columnPreferences: newPreferences }, { merge: true });
    } catch (e) {
        console.error("Failed to save column settings to Firestore", e);
        toast({ title: "Error", description: "Could not save your column preferences.", variant: "destructive" });
    }
  };
  
  useEffect(() => {
      if (isInitialMount.current) {
          isInitialMount.current = false;
      } else {
          if (user) {
              saveColumnSettings(columnOrder, columnVisibility);
          }
      }
  }, [columnOrder, columnVisibility, user]);
  

  useEffect(() => {
    if (!departmentId) return;

    const fetchData = async () => {
      setIsLoading(true);
      try {
        const deptDocRef = doc(db, 'departments', departmentId);
        const projectsSnap = await getDocs(collection(db, 'projects'));
        
        const expensesQuery = query(collection(db, 'expenseRequests'), where('departmentId', '==', departmentId));

        const [deptDocSnap, expensesSnap] = await Promise.all([
          getDoc(deptDocRef),
          getDocs(expensesQuery)
        ]);

        if (deptDocSnap.exists()) {
          setDepartment({ id: deptDocSnap.id, ...deptDocSnap.data() } as Department);
        } else {
          toast({ title: 'Error', description: 'Department not found.', variant: 'destructive' });
        }
        
        setProjects(projectsSnap.docs.map(doc => ({ id: doc.id, ...doc.data() } as Project)));

        const fetchedExpenses = expensesSnap.docs.map(doc => ({ id: doc.id, ...doc.data() } as ExpenseRequest));
        // Sort in the browser instead of in the query
        fetchedExpenses.sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
        setExpenses(fetchedExpenses);
        
      } catch (error: any) {
        console.error("Error fetching data:", error);
        if (error.code === 'failed-precondition') {
             toast({
                title: 'Database Index Required',
                description: "This query requires a custom index. Please check your Firebase console.",
                variant: 'destructive',
                duration: 10000,
             });
        } else {
            toast({ title: 'Error', description: 'Failed to fetch department details.', variant: 'destructive' });
        }
      }
      setIsLoading(false);
    };

    fetchData();
  }, [departmentId, toast]);
  
  const getProjectName = (projectId: string) => {
    return projects.find(p => p.id === projectId)?.projectName || 'Unknown Project';
  }
  
  const visibleHeaders = columnOrder.filter(header => columnVisibility[header]);
  
  const moveColumn = (index: number, direction: 'up' | 'down') => {
      const newOrder = [...columnOrder];
      const newIndex = direction === 'up' ? index - 1 : index + 1;
      
      if (newIndex >= 0 && newIndex < newOrder.length) {
          [newOrder[index], newOrder[newIndex]] = [newOrder[newIndex], newOrder[index]];
          setColumnOrder(newOrder);
      }
  };

  const getCellContent = (header: string, expense: ExpenseRequest) => {
    switch (header) {
      case 'Request No':
        return expense.requestNo;
      case 'Timestamp':
        return expense.createdAt ? format(new Date(expense.createdAt), 'dd MMM yyyy, HH:mm') : 'N/A';
      case 'Generated by Department':
        return expense.generatedByDepartment;
      case 'Project Name':
        return getProjectName(expense.projectId);
      case 'Amount':
        return `₹ ${(expense.amount || 0).toLocaleString()}`;
      case 'Head of A/c':
        return expense.headOfAccount;
      case 'Sub-Head of A/c':
        return expense.subHeadOfAccount;
      case 'Remarks':
        return expense.remarks;
      case 'Description':
        return expense.description;
      case 'Name of the party':
        return expense.partyName;
      default:
        return '';
    }
  };

  if (isLoading && !department) {
    return (
      <div className="w-full px-4 sm:px-6 lg:px-8">
        <div className="mb-6 flex items-center justify-between">
            <div className="flex items-center gap-2">
                <Skeleton className="h-10 w-10" />
                <Skeleton className="h-8 w-64" />
            </div>
            <Skeleton className="h-10 w-36" />
        </div>
        <Card>
            <CardContent className="p-0">
                 <Skeleton className="h-96 w-full" />
            </CardContent>
        </Card>
      </div>
    );
  }

  return (
    <>
    <div className="w-full px-4 sm:px-6 lg:px-8">
        <div className="mb-6 flex items-center justify-between">
            <div className="flex items-center gap-2">
                <Link href="/expenses">
                <Button variant="ghost" size="icon">
                    <ArrowLeft className="h-6 w-6" />
                </Button>
                </Link>
                <h1 className="text-2xl font-bold">
                {department ? `${department.name} Department Expenses` : 'Department Expenses'}
                </h1>
            </div>
            <div className="flex items-center gap-2">
                <Dialog open={isSequenceDialogOpen} onOpenChange={setIsSequenceDialogOpen}>
                    <DialogTrigger asChild>
                        <Button variant="outline">
                            <Shuffle className="mr-2 h-4 w-4" /> Edit Sequence
                        </Button>
                    </DialogTrigger>
                    <DialogContent>
                        <DialogHeader>
                            <DialogTitle>Edit Column Sequence</DialogTitle>
                            <DialogDescription>Use the arrows to reorder the columns. Your changes will be saved automatically.</DialogDescription>
                        </DialogHeader>
                        <div className="py-4 space-y-2">
                            {columnOrder.map((header, index) => (
                                <div key={header} className="flex items-center justify-between p-2 border rounded-md">
                                    <span className="font-medium">({index + 1}) {header}</span>
                                    <div className="flex items-center">
                                        <Button variant="ghost" size="icon" disabled={index === 0} onClick={() => moveColumn(index, 'up')}>
                                            <ArrowUp className="h-4 w-4" />
                                        </Button>
                                        <Button variant="ghost" size="icon" disabled={index === columnOrder.length - 1} onClick={() => moveColumn(index, 'down')}>
                                            <ArrowDown className="h-4 w-4" />
                                        </Button>
                                    </div>
                                </div>
                            ))}
                        </div>
                         <DialogFooter>
                            <DialogClose asChild>
                                <Button>Done</Button>
                            </DialogClose>
                        </DialogFooter>
                    </DialogContent>
                </Dialog>

                <DropdownMenu>
                    <DropdownMenuTrigger asChild>
                        <Button variant="outline">
                            <View className="mr-2 h-4 w-4" />
                            Columns
                        </Button>
                    </DropdownMenuTrigger>
                    <DropdownMenuContent align="end">
                        <DropdownMenuLabel>Toggle columns</DropdownMenuLabel>
                        <DropdownMenuSeparator />
                        {baseTableHeaders.map((header) => (
                            <DropdownMenuCheckboxItem
                                key={header}
                                className="capitalize"
                                checked={columnVisibility[header]}
                                onCheckedChange={(value) =>
                                    setColumnVisibility(prev => ({...prev, [header]: !!value}))
                                }
                            >
                                {header}
                            </DropdownMenuCheckboxItem>
                        ))}
                    </DropdownMenuContent>
                </DropdownMenu>
                <Link href={`/expenses/new-request?departmentId=${departmentId}`}>
                    <Button>
                        <Plus className="mr-2 h-4 w-4"/>
                        New Expense Request
                    </Button>
                </Link>
            </div>
        </div>
      
        <Card>
            <CardContent className="p-0">
                 <div className="overflow-x-auto border rounded-lg">
                    <Table>
                        <TableHeader>
                            <TableRow>
                                {visibleHeaders.map((header) => (
                                  <TableHead key={header} className="whitespace-nowrap px-4">{header}</TableHead>
                                ))}
                            </TableRow>
                        </TableHeader>
                        <TableBody>
                            {isLoading ? (
                                Array.from({ length: 5 }).map((_, i) => (
                                    <TableRow key={i}>
                                        {visibleHeaders.map(header => (
                                            <TableCell key={header}><Skeleton className="h-5 w-full" /></TableCell>
                                        ))}
                                    </TableRow>
                                ))
                            ) : expenses.length > 0 ? (
                                expenses.map(expense => (
                                    <TableRow key={expense.id}>
                                        {visibleHeaders.map(header => (
                                            <TableCell key={header} className="whitespace-nowrap">
                                                {getCellContent(header, expense)}
                                            </TableCell>
                                        ))}
                                    </TableRow>
                                ))
                            ) : (
                                <TableRow>
                                    <TableCell colSpan={visibleHeaders.length} className="text-center h-24">
                                        No expense requests found.
                                    </TableCell>
                                </TableRow>
                            )}
                        </TableBody>
                    </Table>
                </div>
            </CardContent>
        </Card>
    </div>
    </>
  );
}
