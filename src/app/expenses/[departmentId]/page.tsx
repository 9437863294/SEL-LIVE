
'use client';

import { useState, useEffect } from 'react';
import { useParams } from 'next/navigation';
import Link from 'next/link';
import { ArrowLeft, Plus, View } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Skeleton } from '@/components/ui/skeleton';
import { db } from '@/lib/firebase';
import { doc, getDoc } from 'firebase/firestore';
import type { Department } from '@/lib/types';
import { useToast } from '@/hooks/use-toast';
import { Card, CardContent } from '@/components/ui/card';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuCheckboxItem,
  DropdownMenuLabel,
  DropdownMenuSeparator,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import { ResizablePanelGroup, ResizablePanel, ResizableHandle } from '@/components/ui/resizable';


const baseTableHeaders = [
    'Request No',
    'Generated by Department',
    'Project Name',
    'Head of A/c',
    'Sub-Head of A/c',
    'Reception No',
    'Reception Date',
    'Remarks',
    'Description',
    'Name of the party'
];

export default function DepartmentExpensesPage() {
  const params = useParams();
  const { toast } = useToast();
  const departmentId = params.departmentId as string;

  const [department, setDepartment] = useState<Department | null>(null);
  const [isLoading, setIsLoading] = useState(true);

  const [columnVisibility, setColumnVisibility] = useState<Record<string, boolean>>(() => {
    if (typeof window === 'undefined') {
        return baseTableHeaders.reduce((acc, header) => ({ ...acc, [header]: true }), {});
    }
    try {
      const saved = window.localStorage.getItem('expenseColumnVisibility');
      return saved ? JSON.parse(saved) : baseTableHeaders.reduce((acc, header) => ({ ...acc, [header]: true }), {});
    } catch (error) {
      console.error("Failed to parse column visibility from localStorage", error);
      return baseTableHeaders.reduce((acc, header) => ({ ...acc, [header]: true }), {});
    }
  });

  useEffect(() => {
    try {
      window.localStorage.setItem('expenseColumnVisibility', JSON.stringify(columnVisibility));
    } catch (error) {
      console.error("Failed to save column visibility to localStorage", error);
    }
  }, [columnVisibility]);


  useEffect(() => {
    if (!departmentId) return;

    const fetchDepartment = async () => {
      setIsLoading(true);
      try {
        const docRef = doc(db, 'departments', departmentId);
        const docSnap = await getDoc(docRef);
        if (docSnap.exists()) {
          setDepartment({ id: docSnap.id, ...docSnap.data() } as Department);
        } else {
          toast({ title: 'Error', description: 'Department not found.', variant: 'destructive' });
        }
      } catch (error) {
        console.error("Error fetching department:", error);
        toast({ title: 'Error', description: 'Failed to fetch department details.', variant: 'destructive' });
      }
      setIsLoading(false);
    };

    fetchDepartment();
  }, [departmentId, toast]);
  
  const visibleHeaders = baseTableHeaders.filter(header => columnVisibility[header]);

  if (isLoading) {
    return (
      <div className="w-full px-4 sm:px-6 lg:px-8">
        <div className="mb-6 flex items-center justify-between">
            <div className="flex items-center gap-2">
                <Skeleton className="h-10 w-10" />
                <Skeleton className="h-8 w-64" />
            </div>
            <Skeleton className="h-10 w-36" />
        </div>
        <Card>
            <CardContent className="p-0">
                 <Skeleton className="h-96 w-full" />
            </CardContent>
        </Card>
      </div>
    );
  }

  return (
    <div className="w-full px-4 sm:px-6 lg:px-8">
        <div className="mb-6 flex items-center justify-between">
            <div className="flex items-center gap-2">
                <Link href="/expenses">
                <Button variant="ghost" size="icon">
                    <ArrowLeft className="h-6 w-6" />
                </Button>
                </Link>
                <h1 className="text-2xl font-bold">
                {department ? `${department.name} Department Expenses` : 'Department Expenses'}
                </h1>
            </div>
            <div className="flex items-center gap-2">
                <DropdownMenu>
                    <DropdownMenuTrigger asChild>
                        <Button variant="outline">
                            <View className="mr-2 h-4 w-4" />
                            Columns
                        </Button>
                    </DropdownMenuTrigger>
                    <DropdownMenuContent align="end">
                        <DropdownMenuLabel>Toggle columns</DropdownMenuLabel>
                        <DropdownMenuSeparator />
                        {baseTableHeaders.map((header, index) => (
                            <DropdownMenuCheckboxItem
                                key={header}
                                className="capitalize"
                                checked={columnVisibility[header]}
                                onCheckedChange={(value) =>
                                    setColumnVisibility(prev => ({...prev, [header]: !!value}))
                                }
                            >
                                <span className="mr-2 font-mono text-xs w-5 text-center">({index + 1})</span>{header}
                            </DropdownMenuCheckboxItem>
                        ))}
                    </DropdownMenuContent>
                </DropdownMenu>
                <Link href={`/expenses/new-request?departmentId=${departmentId}`}>
                    <Button>
                        <Plus className="mr-2 h-4 w-4"/>
                        New Expense Request
                    </Button>
                </Link>
            </div>
        </div>
      
        <Card>
            <CardContent className="p-0">
                <ResizablePanelGroup direction="horizontal" className="min-w-full rounded-lg border">
                    <ResizablePanel defaultSize={100}>
                         <div className="overflow-x-auto">
                            <Table>
                                <TableHeader>
                                    <TableRow>
                                        {visibleHeaders.map((header) => (
                                          <TableHead key={header} className="whitespace-nowrap px-4">{header}</TableHead>
                                        ))}
                                    </TableRow>
                                </TableHeader>
                                <TableBody>
                                    <TableRow>
                                        <TableCell colSpan={visibleHeaders.length} className="text-center h-24">
                                            No expense requests found.
                                        </TableCell>
                                    </TableRow>
                                </TableBody>
                            </Table>
                        </div>
                    </ResizablePanel>
                    <ResizableHandle withHandle />
                </ResizablePanelGroup>
            </CardContent>
        </Card>
    </div>
  );
}
